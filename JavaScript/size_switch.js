var size = "XL";

switch(size){
    case (size = "S") :
        console.log("Small")
        break;
    case (size = "M"):
        console.log("Medium")
        break;
    case (size = "L"):
        console.log("Large")
        break;
    case (size = "XL"):
        console.log("Extra Large")
        break;
    default:
        console.log("None")


}
